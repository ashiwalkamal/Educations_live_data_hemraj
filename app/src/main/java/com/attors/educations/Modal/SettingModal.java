package com.attors.educations.Modal;

public class SettingModal {
    String txt1;
    int img1;

    public SettingModal(String txt1, int img1) {
        this.txt1 = txt1;
        this.img1 = img1;

    }

    public String getTxt1() {
        return txt1;
    }

    public void setTxt1(String txt1) {
        this.txt1 = txt1;
    }

    public int getImg1() {
        return img1;
    }

    public void setImg1(int img1) {
        this.img1 = img1;
    }


}
