package com.attors.educations.Modal;

public class Weekmodal {
    String txt1,txt2;
    int img1;

    public Weekmodal(String txt1, String txt2, int img1) {
        this.txt1 = txt1;
        this.txt2 = txt2;
        this.img1 = img1;
    }

    public String getTxt1() {
        return txt1;
    }

    public void setTxt1(String txt1) {
        this.txt1 = txt1;
    }

    public String getTxt2() {
        return txt2;
    }

    public void setTxt2(String txt2) {
        this.txt2 = txt2;
    }

    public int getImg1() {
        return img1;
    }

    public void setImg1(int img1) {
        this.img1 = img1;
    }
}
